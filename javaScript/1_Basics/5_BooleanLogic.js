/* Boolean Logic */

let name = 'Morol';
let age = 24;

if (age < 13) {
    console.log(name + ' is a boy.')
}
else if (age >= 13 && age < 20) {
    console.log(name + ' is a teenager.')
}
else if (age >= 20 && age < 30) {
    console.log(name + ' is a young man.')
}
else {
    console.log(name + ' is a man.')
}
// Morol is a young man.
